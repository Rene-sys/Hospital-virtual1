	<I>Esta plataforma digital esta basada en que el usuario tenga acceso a todo tipo de
 	profesionales sanitarios (médicos, enfermeras, psicólogos) utilizando una misma Historia Clínica Electrónica y con una serie de
	 funcionalidades que permiten gestionar al paciente
 	y al profesional los diferentes escenarios clínicos y administrativos (citas on line,
 	consulta y descarga de informes médicos, video consulta, pase de visita on line, 
	teleasistencia urgente,etc).<br>

	<B><br>¿Quiénes podrían ser los beneficiarios del hospital virtual?</B></br>
	Aquellos que por su circunstancia personal y/o laboral les sea muy difícil o incómodo acudir a la cita presencial con su médico. Las videoconsultas son ya una realidad
	 y las diferentes aseguradoras privadas trabajan ya en este terreno.</br>
	
	<B><br>Objetivo Específico</br></B>
	<br>-Diseñar un Sistema Integrado para la Gestionar de una base de datos y a si mejora la eficiencia del Hospital con el fin de generar estrategias que permitan la sostenibilidad.</br>
	<br>-A partir de esto objetivos:</br> 
	<br>-potenciar a las mejoras de una buena salud.</br>
	<br>-implementar nuevos métodos a la hora de procesar la información.</br>
	<br>-establecer la importancias de los datos plenamente empleados en el área de la salud.</br>
	<br>-tener una confidencialidad entre paciente y médico.</br>
	<br>-cuantificar la cantidad recursos de un centro salud.</br>
	<br>-Acceder a la información relevante para el cuidado de su salud.</br>
	<br>-Acceder con más facilidad y rapidez a atención médica general.</br>
	<br>-Iniciar tratamientos de manera más rápida y oportuna.</br>
	<br>-Ahorrar tiempo y desplazamiento innecesarios para conseguir una atención de salud o realizar trámites asociados.</br>

<B><br>Descripción del proyecto</br></B>           
<br>El diseño estará basado en poder resolver las necesidades del país, ya que no cuentan 
con la capacidad de brindar un servicio de forma virtual, pero nosotros relacionamos todas 
las parte de gestión de información de un centro hospitalario de modalidad virtual  como si
 estuviera en una instalación física. De tal manera, que se manejara con un registro de los
 pacientes en sus diferentes consultas médicas y su controles pre-evaluados, que a la vez se
 mantendrá un enfoque de atención en salud, centrado en el paciente, tratando a la vez de 
poder mejorar la calidad de atención que se brinda en las diferentes instalaciones.</br>

<B><br>limitaciones / Restricciones</B></br>
<b>Las limitantes para el usuario pueda tener dentro de esta plataforma digital se dan desde que si Hospital Virtual no tiene costo sino que el paciente solo paga por los servicios que toma y es el médico quien decide el valor de su consulta y los días y horarios que puede atender 
también pueden verse afectados por la área de la cual reside el usuario la limitante en área de su conectividad puede haber pautas ya dentro de la consulta y perdida de el avance clínico 
el paciente debe estar consciente de que la consulta que acaba de recibir la información suministrada pueda demorar un tiempo hasta que se registre en su  historial médico digitalizado 
el paciente solo estará en sala de espera por la cita ya programada y no de otra que no haya sido previamente solicitada 
si por algún motivo el paciente paciente presente mala conectividad o pierde su turno en sala se le agendará como la última persona en ser atendida por la consulta ya requeridas 
los paciente con enfermedades crónicas que soliciten fármacos dentro de la consulta con su médico, se le suministra la información recetaria hacía departamento de farmacia.</br>

